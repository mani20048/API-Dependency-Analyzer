package model;

public class Service {

    private String name;
    private String description;

    public Service() {
        // required for Gson
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
